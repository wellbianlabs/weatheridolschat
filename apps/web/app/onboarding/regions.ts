/** Helper to keep DONG_TABLE rows terse. */
function d(
  sggCode: string,
  sggFullLabel: string,
  dongLabel: string,
  lat: number,
  lng: number,
): DongChoice {
  return {
    id: `${sggCode}_${dongLabel}`,
    sggCode,
    label: dongLabel,
    fullLabel: `${sggFullLabel} ${dongLabel}`,
    lat,
    lng,
  };
}

/**
 * Return the curated 동 list for one 시군구. Returns [] when no
 * curated data exists for that 구 — caller (the onboarding form)
 * collapses the 3rd dropdown in that case.
 */
export function getDongsForSgg(sggCode: string): DongChoice[] {
  return DONG_TABLE.filter((dong) => dong.sggCode === sggCode);
}
