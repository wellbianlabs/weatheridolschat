/**
 * Cascading region picker data: 시/도 → 시군구 → 동.
 *
 * Two layers are reused from existing weather infra:
 *   1. PROVINCES — 17 top-level admin divisions, ordered the way
 *      Koreans expect to see them on a form (Seoul first, then
 *      광역시 in code order, then 도 alphabetically-ish).
 *   2. 시군구 (sggs) — derived from the 155-row KR_SGG_TABLE that
 *      already feeds the KWeather provider. Grouped by province
 *      via the first two digits of the legal code.
 *
 * The third layer (동/dong) is curated by hand here, NOT taken
 * from any existing table. Korea has ~17,000 행정동 nationwide;
 * we ship just the ones our users are likely to live in (Seoul
 * popular districts + major-metro CBDs + a few representative
 * 동 per non-Seoul 구). When a user picks a 구 we don't have any
 * 동 data for, the third dropdown stays empty and we save the
 * 구 centroid as their location — that's an entirely acceptable
 * accuracy floor.
 *
 * Why we don't fetch from a runtime API: the cron-driven weather
 * greeting needs to map a saved (lat, lng) back to a name for the
 * [Now Context] block. A static table guarantees that round-trip
 * is identity-stable and doesn't depend on an external service
 * being up. Cost: 한국 행정구역이 가끔 개편되면 우리도 손으로
 * 따라가야 함 — 빈도 ~1년에 1번 수준이라 OK.
 */

