// ── 시군구 by province ────────────────────────────────────────────────────

export interface SggChoice {
  code: string; // 10-digit legal admin code (matches KR_SGG_TABLE)
  label: string; // short form: "강남구", "수원시" (no province prefix)
  fullLabel: string; // "서울 강남구" — what we save to profiles.primary_label
  lat: number;
  lng: number;
}

/**
 * Get the 시군구 list for a given province. Strips the redundant
 * province prefix from each entry's name ("서울 강남구" → "강남구")
 * so the dropdown reads cleanly once 시/도 is already selected.
 */
export function getSggsForProvince(provinceCode: string): SggChoice[] {
  return KR_SGG_TABLE.filter((sgg) => sgg.code.startsWith(provinceCode)).map((sgg) => {
    // KR_SGG_TABLE names are "서울 강남구" / "부산 해운대구" — split off
    // the leading word so the dropdown shows just "강남구".
    const parts = sgg.name.split(' ');
    const short = parts.length > 1 ? parts.slice(1).join(' ') : sgg.name;
    return {
      code: sgg.code,
      label: short,
      fullLabel: sgg.name,
      lat: sgg.lat,
      lng: sgg.lng,
    };
  });
}

// ── 동 (curated) ──────────────────────────────────────────────────────────

export interface DongChoice {
  /** Stable id derived from sggCode + dongName. */
  id: string;
  /** Parent 시군구 code (matches an entry in KR_SGG_TABLE). */
  sggCode: string;
  /** "역삼동" — short label shown in the dropdown. */
  label: string;
  /** "서울 강남구 역삼동" — what we save to profiles.primary_label. */
  fullLabel: string;
  lat: number;
  lng: number;
}

